package Day17_Branching_While_Do;

public class DoWhileLoop {
    public static void main(String[] args) {
        //1. Even if boolean expression is false the statement inside doWhile loop gets executed once.

        //в вайл лупі перше перевіряється булін експрешин а потім стейтмент інсайд,
        // але в ду вайл луп спочатку виконується стейтмент а потім перевіряється булін експрешин

        boolean bool = false;


        do {
            System.out.println("I am getting executed"); //statement
        }while (bool); //condition // if condition is true the do block will be repeated until condition is false





    }
}
